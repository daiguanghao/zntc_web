package com.eqy.web.pojo;

/**
 * 停车场信息
 * @author daiguanghao
 *
 */
public class PackBean {
	private int id;
	//停车场名称
	private String packName;
	//描述
	private String packDes;
	//地点
	private String packAddress;
	//总共有的车位数
	private int totalNum;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPackName() {
		return packName;
	}
	public void setPackName(String packName) {
		this.packName = packName;
	}
	public String getPackDes() {
		return packDes;
	}
	public void setPackDes(String packDes) {
		this.packDes = packDes;
	}
	public String getPackAddress() {
		return packAddress;
	}
	public void setPackAddress(String packAddress) {
		this.packAddress = packAddress;
	}
	public int getTotalNum() {
		return totalNum;
	}
	public void setTotalNum(int totalNum) {
		this.totalNum = totalNum;
	}
	public int getMacTotalNum() {
		return macTotalNum;
	}
	public void setMacTotalNum(int macTotalNum) {
		this.macTotalNum = macTotalNum;
	}
	public int getGraTotalNum() {
		return graTotalNum;
	}
	public void setGraTotalNum(int graTotalNum) {
		this.graTotalNum = graTotalNum;
	}
	public int getMacTotalUseNum() {
		return macTotalUseNum;
	}
	public void setMacTotalUseNum(int macTotalUseNum) {
		this.macTotalUseNum = macTotalUseNum;
	}
	public int getGraTotalUseNum() {
		return graTotalUseNum;
	}
	public void setGraTotalUseNum(int graTotalUseNum) {
		this.graTotalUseNum = graTotalUseNum;
	}
	//立体车位数
	private int macTotalNum;
	//平面车位数
	private int graTotalNum;
	//立体车位已使用数
	private int macTotalUseNum;
	//平面车位已使用数
	private int graTotalUseNum;
	
}
